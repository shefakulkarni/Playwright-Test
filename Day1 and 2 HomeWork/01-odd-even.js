function oddOrEven(number)
{
    if((number%2)===0)
    {
        console.log("This number is Even:"+number)
    }
    else if((number%2)!==0)
        {
            console.log("This number is Odd:"+number)
        }
        else
        {
             console.log("This is not proper number");
        }
    
}
oddOrEven(110);